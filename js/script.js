document.addEventListener('DOMContentLoaded', () => {
  // Mobile Menu Toggle
  const btn = document.getElementById('mobile-menu-btn');
  const menu = document.getElementById('mobile-menu');
  if (btn && menu) {
    btn.addEventListener('click', () => {
      menu.classList.toggle('hidden');
    });
  }

  // Language Switcher Logic
  const langSelectors = document.querySelectorAll('.language-selector');
  if (langSelectors.length > 0) {
    langSelectors.forEach(selector => {
      selector.addEventListener('change', (e) => {
        setLanguage(e.target.value);
        // Sync all selectors
        langSelectors.forEach(s => s.value = e.target.value);
      });
    });
  }

  // Initialize lang
  const currentLang = localStorage.getItem('siteLang') || 'en';
  if (langSelectors.length > 0) {
    langSelectors.forEach(s => s.value = currentLang);
  }
  setLanguage(currentLang);
});

function setLanguage(lang) {
  if (!translations[lang]) return;
  localStorage.setItem('siteLang', lang);
  
  const elements = document.querySelectorAll('[data-i18n]');
  elements.forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });
}
